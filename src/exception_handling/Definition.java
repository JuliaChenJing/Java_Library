package exception_handling;

public class Definition {
	/*
	 * In Java exceptions under Error and RuntimeException classes are unchecked
	 * exceptions, everything else under throwable is checked.
	 * 
	 * Consider the following Java program. It compiles fine, but it throws
	 * ArithmeticException when run. The compiler allows it to compile, because
	 * ArithmeticException is an unchecked exception.
	 * 
	 * 
	 * Unchecked: “Error” and its subclasses,”RunTimeException” and its
	 * subclasses
	 * 
	 * Checked: Every exception other than Unchecked exception
	 * 
	 * 
	 * Checked Exception: These are the exceptions which may occur regularly in
	 * a program and compiler will check for those exceptions at “Compile Time”
	 * ,those exceptions are called Checked Exceptions
	 * 
	 * Example: FileNotFoundException, EndOfFileException etc.
	 * 
	 * 
	 * Unchecked Exceptions:
	 * 
	 * There are some exceptions which do not occur regularly in a program,and
	 * compiler will not check for those exceptions,these kind of exceptions are
	 * called Unchecked Exceptions
	 * 
	 * Example: ArithmeticException, NullPointerException etc
	 * 
	 * P.S: All the exceptions occurs at “Run Time” only.
	 * 
	 * writing this line because many people think that “Checked Exceptions”
	 * occurs at Compile time and “Unchecked Exceptions” occurs at Run
	 * time,which is not the case.
	 */

}
